var select = document.getElementById("dropdown");
var display = document.getElementById("display-planet");
const content = document.getElementById("display");
const headerHeight = document.getElementById("header").offsetHeight;
var option = document.createElement("option");
option.text = "Select Planet";
option.value = "0";
select.appendChild(option);
data.forEach(element => {
    var option = document.createElement("option");
    option.text = element.name;
    option.value = element.id;
    select.appendChild(option);
});
select.addEventListener("change", function(){
    while (display.hasChildNodes()) {  
        display.removeChild(display.firstChild);
    }
    
    let h1 = document.createElement("h1");
    let id = Number(select.value);
    if(id == 0){
        content.style.marginTop = "0px";
        return;
    }
    id-=1;
    h1.textContent = data[id].name;
    let img = document.createElement("img");
    img.src = data[id].image;
    img.setAttribute("height", "100px");
    img.style.margin = "10px";
    let table = document.createElement("table");
    table.setAttribute("border", "1");
    for(let i = 0; i < planetdata.length; i++){
        let row = document.createElement("tr");
        let cell = document.createElement("td");
        cell.textContent = planetdata[i];
        row.appendChild(cell);
        cell = document.createElement("td");
        cell.innerHTML = data[id][planetdata[i]];
        row.appendChild(cell);
        table.appendChild(row);
    }
    display.appendChild(img);
    display.appendChild(h1);
    display.appendChild(table);
    document.body.style.flexWrap = "wrap";
    content.style.marginTop = (headerHeight + 30) + "px";
});